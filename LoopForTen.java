
public class LoopForTen {

	public static void main(String[] args) {
	
		
			int number;
			int square;
		
			
			
			System.out.println("Number\tSquare");
			
			
			
			for (number=1; number<11; number++)
			{
				square= number * number;
				
				System.out.println(number+"\t"+square);
				
			}
	}


}